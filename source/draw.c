#include "common.h"
#include "draw.h"

fb_t *framebuffer = (fb_t*)0x23FFFE00;

inline u32 fb_sz(u8 *fb)
{
    if (fb == framebuffer->top_left || fb == framebuffer->top_right)
        return TOP_FB_SZ;

    else if (fb == framebuffer->bottom)
        return SUB_FB_SZ;

    else
        return 0; // This should never happen
}

void clear_screen(u8 *fb, u32 rgb)
{
    u32 fb_len = fb_sz(fb),
        i = 0;

    u8  r = rgb & 0xFF,
        g = (rgb >> 8) & 0xFF,
        b = (rgb >> 16) & 0xFF;

    if ((r == g) && (g == b))
    {
        memset(fb, r, fb_len);
        return;
    }

    while (i < fb_len)
    {
        fb[i++] = r;
        fb[i++] = g;
        fb[i++] = b;
    }
}

// Thanks to hartmannaf for letting me know about 3dbrew.org/wiki/TIMER_Registers
inline void delay(const u16 n)
{
    if (n == 0) // In case delay is 0
        return;

    REG_TM0VAL = 0; // Reset the timer
    REG_TM0CNT = 0x87; // Start timer count

    while(REG_TM0VAL < n); // Delay n ticks

    REG_TM0CNT = 0x07; // Stop timer count
}

inline void set_pixel(u8 *fb, const u16 x, const u16 y, const u32 rgb)
{
    if (y > 239 || x > (fb == framebuffer->top_left ? 399 : 319))
        return;

    u32 offset = ((240 * x) + 239 - y) * 3;
    fb[offset++] = rgb & 0xFF;
    fb[offset++] = (rgb >> 8) & 0xFF;
    fb[offset++] = (rgb >> 16) & 0xFF;
}

void draw_str(const u8 *fb, const char *str, const u16 x, const u16 y, const u32 rgb)
{
    u16 _x = x;
    u8 i = 0;

    while(str[i] != '\0')
    {
        draw_char (fb, _x, y, str[i++], rgb);
        _x += 8;
    }
}

void draw_char(const u8 *fb, const u16 x, const u16 y, const u8 c, const u32 rgb)
{
    u16 _x, _y, _c;

    if (c < 0x20 || c > 0x7F) // Only printable characters allowed!
        return;

    _c = (c - 0x20) << 3;

    for(_y = 0; _y < 8; _y++)
    {
        u8 mask = 0x80;
        u8 row = font[_y + _c];
        for(_x = 0; _x < 8; _x++, mask >>= 1)
            if(row & mask)
                set_pixel((u8*)fb, x + _x, y + _y, rgb);
    }
}

void error(const char *msg)
{
    clear_screen(framebuffer->top_left, 0);
    draw_str(framebuffer->top_left, msg, 8, 8, 0xFFFFFF);
    draw_str(framebuffer->top_left, "Press any key to power off", 8, 24, 0xFFFFFF);
    while(!HID_PAD);
    poweroff();
}

